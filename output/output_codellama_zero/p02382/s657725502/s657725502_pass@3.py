import math
n =int(input())
X=list(map(int, input().split()))
Y=list(map(int, input().split()))
s1 = sum(abs(X[i]-Y[i]) for i in range(n))
s2 = sum((abs(X[i]-Y[i])**2) for i in range(n))
s3 = sum((abs(X[i]-Y[i])**3) for i in range(n))
s4max = max(abs(X[i]-Y[i]) for i in range(n))
print('{:.6f}'.format(s1))
print('{:.6f}'.format(math.sqrt(s2)))
print('{:.6f}'.format(math.pow(s3, 1/3)))
print('{:.6f}'.format(s4max))